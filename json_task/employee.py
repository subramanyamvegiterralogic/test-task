class Employee:

    def __init__(self):
        pass

    def get_esal(self):
        return self.esal
    def set_esal(self, esal):
        self.esal = esal

    def get_no(self):
        return self.eno
    def set_no(self, eno):
        self.eno= eno

    def get_city(self):
        return self.ecity
    def set_city(self, ecity):
        self.ecity = ecity

    def get_mobile(self):
        return self.emobile
    def set_mobile(self, emobile):
        self.emobile = emobile

    def get_name(self):
        return self.ename
    def set_name(self, ename):
        self.ename= ename
